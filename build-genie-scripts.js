#!/usr/bin/env node
/*
 * build-genie-scripts.js — converts Kyra's genie-scripts.md into js/genie-scripts-data.js.
 *
 * Source of truth is genie-scripts.md. This emits window.GENIE_SCRIPTS keyed by
 * category bucket -> angle id -> { hook, build, turn, close } arrays of lines.
 * NEVER hand-edit js/genie-scripts-data.js — edit the MD and re-run this.
 *
 * The MD is human-written and messy: smart quotes, occasional missing quote marks,
 * section headers sometimes glued to the end of the previous line, stray colons.
 * The parser below is deliberately lenient and validates the result at the end.
 */
const fs = require('fs');
const path = require('path');

const SRC = path.join(__dirname, 'genie-scripts.md');
const OUT = path.join(__dirname, 'js', 'genie-scripts-data.js');

// CATEGORY header (uppercase, standalone) -> output bucket key (TitleCase)
const CATEGORY = {
  BEAUTY: 'Beauty', FASHION: 'Fashion', ELECTRONICS: 'Electronics', HOME: 'Home',
  HEALTH: 'Health', SPORTS: 'Sports', KITCHEN: 'Kitchen', PETS: 'Pets',
  ACCESSORIES: 'Accessories', FOOD: 'Food', TOYS: 'Toys'
};

// Angle header (case-insensitive, colon optional) -> angle id used by genie.js
const ANGLE = {
  'generic': 'balanced', 'storytime': 'storytime', 'educational': 'educational',
  'before/after': 'beforeafter', 'product demo': 'demo', 'skit/comedy': 'skit'
};

// Section header keyword -> object key. Order: multi-word first so "THE BUILD" wins over a HOOK scan.
const SECTIONS = [
  ['THE BUILD', 'build'], ['THE TURN', 'turn'], ['THE CLOSE', 'close'], ['HOOK', 'hook']
];

function stripColon(s) { return s.replace(/:\s*$/, '').trim(); }

function cleanLine(s) {
  let t = s.trim();
  t = t.replace(/^[-•*]\s*/, '');                 // stray bullet markers
  t = t.replace(/^[“"'‘’]+/, '').replace(/[”"'‘’]+$/, ''); // surrounding quotes (straight or smart)
  return t.trim();
}

// Find the first section header in a line; returns {idx, len, key} or null.
function findSection(line) {
  let best = null;
  for (const [kw, key] of SECTIONS) {
    const i = line.indexOf(kw);
    if (i !== -1 && (best === null || i < best.idx)) best = { idx: i, len: kw.length, key: key };
  }
  return best;
}

const raw = fs.readFileSync(SRC, 'utf8').split(/\r?\n/);
const data = {};
let cat = null, angle = null, section = null;

function ensure() {
  if (!data[cat]) data[cat] = {};
  if (!data[cat][angle]) data[cat][angle] = { hook: [], build: [], turn: [], close: [] };
}
function push(text) {
  const c = cleanLine(text);
  if (!c) return;
  if (!cat || !angle || !section) return;          // stray line outside a section — ignore
  ensure();
  data[cat][angle][section].push(c);
}

for (let rawLine of raw) {
  const line = rawLine.trim();
  if (!line) continue;

  // CATEGORY header (exact uppercase membership)
  const up = line.replace(/:\s*$/, '').toUpperCase();
  if (CATEGORY[up] && line === line.toUpperCase()) {
    cat = CATEGORY[up]; angle = null; section = null;
    continue;
  }

  // ANGLE header
  const ang = ANGLE[stripColon(line).toLowerCase()];
  if (ang) { angle = ang; section = null; ensure(); continue; }

  // SECTION header — may be glued to the end of a content line
  const sec = findSection(line);
  if (sec) {
    const before = line.slice(0, sec.idx).trim();
    const after = line.slice(sec.idx + sec.len).replace(/^:\s*/, '').trim();
    if (before) push(before);                       // tail of the previous section
    section = sec.key;
    if (after) push(after);                         // content trailing the header (rare)
    continue;
  }

  // plain content line
  push(line);
}

// ---- validate ----
const cats = Object.keys(data);
const expectedCats = Object.values(CATEGORY);
const expectedAngles = Array.from(new Set(Object.values(ANGLE)));
const problems = [];
for (const c of expectedCats) {
  if (!data[c]) { problems.push(`MISSING category: ${c}`); continue; }
  for (const a of expectedAngles) {
    const s = data[c][a];
    if (!s) { problems.push(`MISSING angle: ${c}/${a}`); continue; }
    for (const k of ['hook', 'build', 'turn', 'close']) {
      if (!s[k] || !s[k].length) problems.push(`EMPTY section: ${c}/${a}/${k}`);
    }
  }
}

// ---- report ----
console.log(`Parsed ${cats.length} categories from genie-scripts.md\n`);
for (const c of expectedCats) {
  if (!data[c]) continue;
  const counts = expectedAngles.map(a => {
    const s = data[c][a]; if (!s) return `${a}:—`;
    return `${a}:${s.hook.length}/${s.build.length}/${s.turn.length}/${s.close.length}`;
  }).join('  ');
  console.log(`  ${c.padEnd(12)} ${counts}`);
}
console.log('\n(angle: hook/build/turn/close line counts)');

if (problems.length) {
  console.error('\n❌ VALIDATION FAILED:\n  ' + problems.join('\n  '));
  process.exit(1);
}

const header =
  '// GENERATED by build-genie-scripts.js from genie-scripts.md — DO NOT EDIT BY HAND.\n' +
  '// Edit genie-scripts.md and re-run: node build-genie-scripts.js\n' +
  `// Generated: ${new Date().toISOString()}\n` +
  `// ${expectedCats.length} categories x ${expectedAngles.length} angles x 4 sections (HOOK/BUILD/TURN/CLOSE)\n\n`;
fs.writeFileSync(OUT, header + 'window.GENIE_SCRIPTS = ' + JSON.stringify(data, null, 2) + ';\n');
console.log(`\n✅ Wrote ${OUT}`);
